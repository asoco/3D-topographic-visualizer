import * as THREE from 'three';

export class Utils {
    static createSphere(position, color, name = 'marker') {
        const geometry = new THREE.SphereGeometry(0.1);
        const material = new THREE.MeshBasicMaterial({
            color: color,
        });
        const sphere = new THREE.Mesh(geometry, material);
        sphere.position.copy(position);
        sphere.name = name;
        return sphere;
    }

    static getIntersectedObjectsFromEmitedRay(raycaster, scene, mouse, camera) {
        raycaster.setFromCamera(mouse, camera);
        
        let intersections = []
        scene.children.filter(el => el.isPoints).forEach(points => {
            const intersects = raycaster.intersectObject(points, true);

            if (intersects.length) {
                intersections.push(intersects[0]);
            }    
        });
        intersections.sort((a, b) => a.distanceToRay - b.distanceToRay);

        return intersections;
    }

    static selectObjectsByNames(scene, ...names) {
        let selectedObjects = [];
        scene.traverse(el => {if (names.includes(el.name)) selectedObjects.push(el)})
        return selectedObjects;
    }
}

